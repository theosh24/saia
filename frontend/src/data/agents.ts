export function slugify(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

export function getAgentBySlug(agents: Agent[], slug: string): Agent | undefined {
  return agents.find((a) => slugify(a.name) === slug || a.slug === slug);
}

